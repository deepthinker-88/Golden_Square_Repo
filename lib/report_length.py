def report_length(str):
    length = len(str)
    return f"This string was{length} characters long."


def report_length_upper(str):
    upper_length = str.upper()
    return f"{str} in capitals is equivalent to {upper_length}"


def report_length_lower(str):
    lower_length = str.lower()
    return f"{str} in lower case equals {lower_length}"